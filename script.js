function createRadarBackground(count) {
    let html = '';
    for (let i = 1; i <= 5; i++) {
        const r = 80 * (i / 5);
        html += `<circle cx="100" cy="100" r="${r}" fill="none" stroke="rgba(255,255,255,0.1)" stroke-width="1"/>`;
    }
    return html;
}

function createRadarPath(data, count) {
    const angleStep = 360 / count;
    const points = data.map((val, i) => {
        const angle = (angleStep * i - 90) * (Math.PI / 180);
        const r = (val / 100) * 80;
        return `${100 + r * Math.cos(angle)},${100 + r * Math.sin(angle)}`;
    });
    return `<polygon points="${points.join(' ')}" fill="rgba(139, 92, 246, 0.3)" stroke="#8b5cf6" stroke-width="2"/>`;
}

function createRadarLabels(dims) {
    const angleStep = 360 / dims.length;
    return dims.map((d, i) => {
        const angle = (angleStep * i - 90) * (Math.PI / 180);
        const x = 100 + 95 * Math.cos(angle);
        const y = 100 + 95 * Math.sin(angle);
        return `<text x="${x}" y="${y}" text-anchor="middle" dominant-baseline="middle" fill="white" font-size="10" font-weight="bold">${d.name}</text>`;
    }).join('');
}


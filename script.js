function nextQuestion() {
    if (answers[currentQuestionIndex] === undefined) return;
    
    const q = questions[currentMode][currentQuestionIndex];
    if (!scores[q.dimension]) scores[q.dimension] = 0;
    scores[q.dimension] += q.options[answers[currentQuestionIndex]].score;
    
    if (currentQuestionIndex < questions[currentMode].length - 1) {
        currentQuestionIndex++;
        saveProgress();
        
        anime({
            targets: '#question-container',
            translateX: [30, 0],
            opacity: [0, 1],
            duration: 400,
            easing: 'easeOutExpo'
        });
        
        renderQuestion();
    } else {
        finishTest();
    }
}

function prevQuestion() {
    if (currentQuestionIndex > 0) {
        const q = questions[currentMode][currentQuestionIndex];
        if (answers[currentQuestionIndex] !== undefined) {
            scores[q.dimension] -= q.options[answers[currentQuestionIndex]].score;
        }
        
        currentQuestionIndex--;
        saveProgress();
        
        anime({
            targets: '#question-container',
            translateX: [-30, 0],
            opacity: [0, 1],
            duration: 400,
            easing: 'easeOutExpo'
        });
        
        renderQuestion();
    }
}

function finishTest() {
    clearProgress();
    calculateFinalScores();
    showResults();
    
    confetti({
        particleCount: 100,
        spread: 70,
        origin: { y: 0.6 },
        colors: ['#8b5cf6', '#ec4899', '#00f0ff', '#fbbf24']
    });
}

function calculateFinalScores() {
    const maxScores = {};
    questions[currentMode].forEach(q => {
        maxScores[q.dimension] = (maxScores[q.dimension] || 0) + 5;
    });
    
    Object.keys(scores).forEach(k => {
        const max = maxScores[k] || 20;
        scores[k] = Math.round((scores[k] / max) * 100);
    });
}


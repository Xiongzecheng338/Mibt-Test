function getStarRating() {
    const avg = Object.values(scores).reduce((a, b) => a + b, 0) / Object.values(scores).length;
    return Math.min(5, Math.floor(avg / 20) + 1);
}

function getRecommendation(type) {
    const recs = new Set();
    Object.keys(scores).forEach(k => {
        const score = scores[k];
        const key = score > 60 ? `high_${k}` : (score < 40 ? `low_${k}` : null);
        if (key && recommendations[type][key]) {
            recommendations[type][key].forEach(item => recs.add(item));
        }
    });
    return Array.from(recs).slice(0, 9);
}

